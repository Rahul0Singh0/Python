# range(start, stop, step)
for i in range(1, 11, 3):
    print(i)

# iterate over a string
print("-----------------------------------------\n")
name = input("Enter your name: ");
for ch in name:
    print(ch)


# iterate over a list
print("-----------------------------------------\n")
numbers = [10, 20, 30]

for n in numbers:
    print(n)


# while loop
print("-----------------------------------------\n")
count = 1

while count <= 5:
    print(count)
    count += 1
